#operadores aritméticos

adicao = 10 + 10
print("Adição:", adicao)

subtracao = 10 - 10
print("Subtração:", subtracao)

multiplicacao = 10 * 10
print("Multiplicação:", multiplicacao)

divisao = 10 / 2.2 #sempre sera float 
print("Divisão:", divisao)

divisao_inteira = 10 // 2.2  #sem casa decimais (Inteiro)
print("Divisão inteira:", divisao_inteira)

exponenciacao = 2 ** 10
print ("Exponenciacão", exponenciacao)

modulo = 55 % 2 #Resto da divisão 
print("Modulo", modulo)